
public class MathPowerClass {
	void squarePower(int num) {
		double sq = Math.pow(num, 2);
		System.out.println("Square = "+sq);
	}
	void cubePower(int num) {
		double sq = Math.pow(num, 3);
		System.out.println("Cube = "+sq);
	}
	void fourthPower(int num) {
		double sq = Math.pow(num, 4);
		System.out.println("Fourth Power = "+sq);
	}

}
